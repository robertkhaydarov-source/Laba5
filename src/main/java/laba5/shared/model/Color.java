package laba5.shared.model;

import java.io.Serializable;

/**
 * Перечисление цветов, используемых для описания внешности человека.
 */
public enum Color implements Serializable {

    /** Зелёный цвет. */
    GREEN,

    /** Красный цвет. */
    RED,

    /** Чёрный цвет. */
    BLACK,

    /** Оранжевый цвет. */
    ORANGE,

    /** Белый цвет. */
    WHITE;
}
